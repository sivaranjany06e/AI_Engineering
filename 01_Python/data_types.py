# ==========================================
# Python Data Types
# ==========================================

# String
name = "Sivaranjany"

# Integer
age = 29

# Float
salary = 10.5

# Boolean
is_learning_ai = True

# List
skills = ["Python", "Git", "NumPy"]

# Tuple
location = ("Tamil Nadu", "India")

# Set
numbers = {10, 20, 30}

# Dictionary
student = {
    "name": "Sivaranjany",
    "role": "AI Engineer",
    "experience": 2.5
}

print(type(name))
print(type(age))
print(type(salary))
print(type(is_learning_ai))
print(type(skills))
print(type(location))
print(type(numbers))
print(type(student))

